@app.route('/')
def home():
    # Retrieve full details of courses the student is enrolled in
    my_schedule_details = [c for c in courses if c['id'] in my_schedule]
    total_credits = sum(c['credits'] for c in my_schedule_details)

    return render_template_string(
        HTML_TEMPLATE,
        courses=courses,
        my_schedule=my_schedule,
        my_schedule_details=my_schedule_details,
        total_credits=total_credits
    )

@app.route('/register/<int:course_id>', methods=['POST'])
def register(course_id):
    # Find the course object
    course = next((c for c in courses if c['id'] == course_id), None)

    if not course:
        flash("Course not found.", "danger")
        return redirect(url_for('home'))

    # Validation Logic
    if course_id in my_schedule:
        flash(f"You are already registered for {course['code']}.", "warning")
    elif course['enrolled'] >= course['capacity']:
        flash(f"Sorry, {course['code']} is currently full.", "danger")
    else:
        # Success Logic
        course['enrolled'] += 1
        my_schedule.append(course_id)
        flash(f"Successfully registered for {course['name']}!", "success")

    return redirect(url_for('home'))

@app.route('/reset', methods=['POST'])
def reset():
    # Logic to drop all classes (for testing purposes)
    global my_schedule
    for course_id in my_schedule:
        course = next((c for c in courses if c['id'] == course_id), None)
        if course:
            course['enrolled'] -= 1
    my_schedule = []
    flash("Schedule cleared.", "info")
    return redirect(url_for('home'))

